        private void Button_Click(object sender, RoutedEventArgs e)
        {
            User user = User.GetUser(Name.Text, Password.Password);
            if (user == null)
                MessageBox.Show("Access denied");
            else
            {
                new Bank_Application().Show();
                this.Close();
            }
                

